export function generateRecipePrompt(filters: string[]): string {
  return `
Ты — опытный повар с ИИ-помощью. На основе списка доступных продуктов и пользовательских ограничений предложи 2–3 рецепта, которые можно приготовить прямо сейчас.

Важно:
- Не используй ингредиенты, которых нет в списке.
- Учитывай фильтры (например, без мяса, быстро, сладкое и т.д.).
- Рецепты должны быть реалистичными и краткими.

Вот данные пользователя:

Ингредиенты:
${COMMON_INGREDIENTS.map((item) => `- ${item}`).join("\n")}

Ограничения:
${filters.length > 0 ? filters.map((f) => `- ${f}`).join("\n") : "нет"}

Ответ верни строго в следующем формате (JSON-массив из объектов):

[
  {
    "title": "Название блюда",
    "cookingTime": "примерное время (например: 15 минут)",
    "ingredients": ["ингредиент 1", "ингредиент 2", "..."],
    "steps": ["Шаг 1", "Шаг 2", "..."],
    "tips": ["Совет 1", "Совет 2"]
  }
]

Если подходящих рецептов нет — верни пустой массив \`[]\`.
`.trim();
}
